// Criação do array
let cidades = ['São Paulo', 'Rio de Janeiro', 'Belo Horizonte', 'Salvador', 'Fortaleza'];

// Usando o método splice() para remover as cidades 'Belo Horizonte' e 'Salvador'
// O primeiro parâmetro indica o índice inicial, e o segundo parâmetro indica quantos elementos remover
cidades.splice(2, 2); // Remove a partir do índice 2, removendo 2 elementos

// Exibindo o array resultante
console.log(cidades);
